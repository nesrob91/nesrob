public class Regla {
    private int idRegla;
    private int idTipoRegla;
    private int prioridad;

    public int getIdRegla() {
        return idRegla;
    }

    public void setIdRegla(int idRegla) {
        this.idRegla = idRegla;
    }

    public int getIdTipoRegla() {
        return idTipoRegla;
    }

    public void setIdTipoRegla(int idTipoRegla) {
        this.idTipoRegla = idTipoRegla;
    }

    public int getPrioridad() {
        return prioridad;
    }

    public void setPrioridad(int prioridad) {
        this.prioridad = prioridad;
    }
    
    
}
